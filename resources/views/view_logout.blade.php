    <h4>Majanemen Surat</h4>
       Manajemen surat adalah proses pengelolaan surat masuk 
       dan surat keluar yang dilakukan secara sistematis, 
       mulai dari penerimaan, pencatatan, pengklasifikasian,
        pendistribusian, penyimpanan, hingga pengarsapan,
         agar informasi yang terdapat dalam surat dapat digunakan secara efektif,
          efisien, dan mudah ditemukan kembali ketika diperlukan.
    <p>
    <h4>Tujuan Surat</h4>
        Dalam instansi pemerintah maupun organisasi, surat manajemen bertujuan untuk:<br>

1. Menjamin kelancaran arus informasi.<br>
2. Mempermudah proses administrasi dan pelayanan.<br>
3.Menjaga keamanan serta kerahasiaan dokumen.<br>
4.Memudahkan pencarian kembali surat sebagai arsip.<br>
5. Membantu tertib administrasi dan akuntabilitas organisasi.<br>

Secara sederhana, manajemen surat merupakan serangkaian kegiatan untuk
 mengelola surat masuk dan surat keluar agar proses komunikasi
 administrasi dalam organisasi berjalan tertib, cepat, tepat, 
 dan terdokumentasi dengan baik.
 
